#include <iostream>
using namespace std;

int main() {
    int a, b, sum;
    cout << "Enter an integer a = " << endl;
    cin >> a;
    cout << "Enter an integer b = " << endl;
    cin >> b;
    sum = a + b;
    cout << "The sum of " << a << " and " << b << " is " << sum << endl;
    return 0;
}
